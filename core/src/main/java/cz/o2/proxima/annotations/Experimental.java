package cz.o2.proxima.annotations;

import java.lang.annotation.Documented;
import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * This annotation is used on experimental features and implementations. After the feature is tested
 * the annotation is either changed to {@link Stable} or the feature is removed.
 *
 * <p>Note that experimental features need not to keep backward compatibility even between minor
 * updates.
 */
@Target(ElementType.TYPE)
@Retention(RetentionPolicy.SOURCE)
@Documented
public @interface Experimental {

  String value() default "";
}
